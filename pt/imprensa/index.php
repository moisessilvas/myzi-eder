        <p>Jovem maestrina Mariana Menezes rege a OSTNCS.</p>

        <p>É comum maestro jovem ser recebido com certas reservas e desconfianças pelo público. Mais ainda, quando se trata de mulher. E quando se juntam esses dois fatores – juventude e sexo feminino – aparentemente desfavoráveis, e ainda apresentando obras de compositoras , o papel da regente é superdifícil. <b>Mas na noite de 27 de agosto, no Cine Brasília, a maestrina Mariana Menezes de somente 30 anos de idade, transformou essas expectativas negativas num grande sucesso.</b></p>

        <p>Bacharel em Regência pela UnB, ela continuou estudando no Brasil e no exterior, colecionando diplomas, certificados, e fazendo cursos com maestros famosos. A primeira apresentação pública da Mariana foi em 2007, quando regeu um coro de metais na solenidade de entrega do título de professor Emérito da UnB a este colunista. Outras apresentações se seguiram em palcos nacionais e internacionais. E finalmente, Mariana teve a oportunidade de brindar com sua arte a cidade mãe, Brasília.</p>

        <p>Nesse concerto, ela privilegiou quatro compositoras, começando com: <br>
          Fanny Mendelssohn Hensel, irmã do famoso compositor Felix Mendelssohn, com Abertura em Dó Maior. A parte lenta inicial ganhou expressividade com gestos flexíveis e suaves, típicos das mulheres sensíveis. A parte mais rítmica necessitou gestos mais firmes, porém não duros. <b>Já nessa primeira peça, a maestrina mostrou o domínio da técnica das mãos, colocada a serviço da interpretação condizente com o caráter da obra.</b>
        </p>

        <p>Em seguida, Concertino para flauta e orquestra, composto em 1902 pela francesa Cecile Chaminade com a finalidade de dar aos flautistas a oportunidade de mostrar virtuosismo. A execução, com boa afinação, coube a Mechthild Bier, primeira flauta solo da própria orquestra, que interpretou com brilho as difíceis passagens técnicas. Mostrou som bonito, porém, pouco expressivo – até tímido – fato que depreciou um pouco o papel da solista em relação a orquestra, cujo papel foi de simples acompanhante.</p>

        <p> A terceira peça privilegiou a música contemporânea. A escolhida foi a autora paraense Cibele J. Donza com Pasargada em Noites e Luzes, estreada em 2017, em Belém. Linguagem do século XX, muita percussão e efeitos sonoros. Inspirada no poema cujo tópicos seriam – conforme o programa – calor, noites frias, dança, luzes e fogo, é uma obra pragmática, interessante, bem feita dentro do estilo escolhido, mas, apesar de todo esforço dos músicos e da maestrina, este colunista, talvez um pouca antiquado, não conseguiu sentir as emoções indicadas no programa. </p>

        <p>A coroação foi a Sinfonia em mi menor – Gaelic, de autoria de Amy Beach, primeira sinfonia escrita por um compositor norte-americano (1894). A fonte da obra foram canções folclóricas irlandesas desenvolvidas nos quatro movimentos. Sem dúvida uma obra interessante, agradável, fácil de ouvir, mas nem tão fácil de tocar e muito menos de reger, porém sem a profundidade de um Brahms ou Beethoven. Aqui, a maestrina mostrou qualidades moldando a sonoridade da orquestra, dando liberdade aos solistas, entre quais se destacaram as trompas. Ela construiu a obra, deu sua cara, imprimiu sua concepção.</p>

        <p>A maestrina Mariana Menezes, fisicamente jovem, mas adulta artisticamente, provou que talento, estudos, dedicação e esforços resultam num amadurecimento artístico característico dos grandes mestres. Memória prodigiosa, ela ensaiou e regeu todo o programa de cor. A delicadeza no trato com os músicos e o conhecimento profundo, até o último detalhe, provocou a admiração dos músicos e do público que aplaudiu em pé. Agradou também, e principalmente, a apresentação de um programa diferente, preparado em quatro dias, só de compositoras, provando que a igualdade entre homens e mulheres existe também na música, e que ela se conquista com trabalho e resultados, e não com passeatas.</p>

