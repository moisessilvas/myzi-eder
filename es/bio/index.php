        <h2 style="text-align: center; font-family: poppins;">Biografía</h2>
        <p>Mariana Menezes nació en Uberaba, ciudad del estado brasileño de Minas Gerais, donde inició sus estudios a los seis años y se diplomó en flauta traversa en el Conservatorio de Música Renato Frateschi. Es licenciada en música con mención en dirección de orquesta  por la Universidad de Brasilia, máster en dirección orquestal por la Universidad de Manitoba (Canadá) y especialista en dicho tema por la Academia de Dirección Orquestal de la Orquesta Sinfónica del Estado de São Paulo (OSESP).</p>

        <p>Mariana, que tuvo sus primeras experiencias con dirección de orquesta  a los 13 años, estudió con maestros de varios países, como Portugal, Japón, Reino Unido, Italia, Estonia, Argentina, Argentina, Alemania y Estados Unidos. Completó su formación con diversos cursos, master classes y talleres con músicos de reconocimiento internacional, como Riccardo Muti (titular de la Chicago Symphony Orchestra), Colin Metters (profesor emérito de la Royal Academy of Music de Londres), Arvo Volmer (Adelaide Symphony Orchestra, Australia), Giancarlo Guerrero (Nashville Symphony y Cleveland Orchestra) y Neil Thomson (Orquesta Filarmónica de Goiás). En 2016, Mariana fue la única mujer seleccionada en la categoría de dirección en el competitivo Festival Internacional de Invierno de Campos do Jordão.</p>

        <p>También en el 2016, Mariana participó de la primera edición de la Academia de Regencia de la OSESP, donde trabajó durante todo el año bajo mentoría de Marin Alsop – directora titular de la OSESP y de la Baltimore Symphony Orchestra.</p>

        <p>Reconocida como una de las más jóvenes maestras de destaque en el escenario musical brasileño actual, Mariana ya estuvo en el podio de grandes orquestas, como las Orquestas Sinfónicas del Estado de São Paulo (OSESP) y de Heliópolis – en la sala São Paulo –, así como la Orquesta de la Costa Atlántica (Portugal) y las Orquestas Sinfónicas de Santo André, de João Pessoa, del Teatro Nacional Claudio Santoro y del Theatro da Paz.</p>

        <p>Desde el 2016, Mariana es directora residente en la Sociedad de Conciertos de Brasília (SCB), donde colabora con proyectos, programación y producción musical. Además, también actuó como profesora de dirección de orquesta en la Universidad de Brasilia en el 2019 y ya grabó en video un curso de conducción musical que muy pronto será lanzado en la plataforma digital Musicalll. </p>
