        <b><p style="text-align: center">Crítica de Bohumil Med, autor best-seller, crítico musical nacido en la antigua Checoslovaquia y residente de Brasilia.</p></b>

        <p>Joven maestra Mariana Menezes dirige a la Orquesta Sinfónica del Teatro Nacional Claudio Santoro (OSTNCS)</p>

        <p>Es común que jóvenes maestros sean recibidos con reserva y escepticismo por el público. Más aún cuando se trata de una mujer. Y cuando una directora de orquesta suma ambas características aparentemente desfavorables – es joven y mujer –, presentando, además, obras de otras mujeres, su papel es superdifícil. Sin embargo, en la noche del 27 de agosto, en el Cine Brasília, la maestra Mariana Menezes, de tan solo 30 años, ha transformado esas expectativas negativas en un gran éxito.</b></p>

        <p>Licenciada en Dirección de Orquesta  por la Universidad de Brasilia, Mariana siguió estudiando en Brasil y en el exterior, coleccionando diplomas, certificaciones y cursos con famosos maestros. Su primera presentación pública fue en el 2007, cuando dirigió a un coro de metales en la ceremonia de otorgamiento del título de profesor emérito de la UnB a este columnista. A esa, siguieron otras presentaciones en escenarios nacionales e internacionales, hasta que pudo, finalmente, brindar con su arte a la ciudad madre, Brasilia.</p>

        <p>En ese concierto, Mariana privilegió a cuatro compositoras, empezando por Fanny Mendelssohn Hensel, hermana del famoso compositor Felix Mendelssohn, con Obertura en Do Mayor. La parte lenta inicial ganó expresividad con gestos flexibles y suaves, típicos de las mujeres sensibles. La parte más rítmica, por otro lado, exigió gestos más firmes, pero no duros. Ya en la primera pieza, la maestra demostró dominar la técnica de manos, empleada a servicio de una interpretación coherente con el carácter de la obra. </p>

        <p>A continuación, condujo Concertino para flauta y orquesta, compuesto en 1902 por la francesa Cecile Chaminade, con propósito de dar a los flautistas la oportunidad de mostrar su virtuosismo. La ejecución, de buena afinación, le tocó a Mechthild Bier, primera flauta solista de la orquesta, que interpretó con resplandor los difíciles pasajes técnicos. Mostró un bello sonido; sin embargo, poco expresivo – hasta tímido –, lo que depreció un poco el papel de la solista en relación con la orquesta, cuya función fue la de simple acompañante.</p>

        <p>La tercera pieza privilegió a la música contemporánea. La elegida fue la autora brasileña, del estado de Pará, Cibele J. Donza, con su obra “Pasargada en noches y luces”, que estrenó en el 2017, en Belém, capital de dicho estado. Lenguaje del siglo XX, mucha percusión y efectos sonoros. Inspirada en un poema cuyos temas serían – de acuerdo con la programación – calor, noches frías, danza, luces y fuego, esa es una obra pragmática, interesante y muy bien hecha de acuerdo con el estilo escogido. Sin embargo, a pesar de todo el esfuerzo de los músicos y de quien los dirigió, este columnista, tal vez un poco anticuado, no pudo sentir las emociones indicadas en la programación.</p>

        <p>El cierre estuvo a cargo de la Sinfonía en mi menor/ Sinfonía Gaélica, de Amy Beach, primera sinfonía escrita por un compositor estadounidense, en 1894. Las referencias de la obra fueron canciones folclóricas irlandesas desarrolladas en los cuatro movimientos. Sin duda, es una obra interesante, agradable y fácil de oírse, pero no tan fácil de ejecutarse y tampoco de dirigir; no obstante, sin la profundidad de un Brahms o Beethoven.  En ese caso, Mariana demostró sus cualidades dando forma a la sonoridad de la orquesta y dando libertad a los solistas, entre los cuales se destacaron las trompas. Mariana construyó la obra, le dio su toque y le imprimió su concepción.</p>

        <p>La maestra Mariana Menezes, físicamente joven pero artísticamente adulta, ha demostrado que talento, estudio, dedicación y esfuerzos resultan en la madurez artística que caracteriza a los grandes maestros. Portadora de una memoria prodigiosa, ella ensayó y dirigió al dedillo toda la programación. La delicadeza con que trata a los músicos y el conocimiento profundo, hasta el último detalle, provocaron la admiración de los músicos y del público, que la aplaudió de pie. Agradó también – y principalmente – la presentación de un programa diferente, preparado en cuatro días, exclusivamente de compositoras, probando que la igualdad entre hombres y mujeres existe también en la música, igualdad que se conquista con trabajo y resultados, y no con protestas.</p>

        <p>Felicitamos a la joven maestra, que, seguramente, logrará grandes éxitos, no solo en la provinciana Brasilia, sino en los centros nacionales e internacionales de cultura musical. Así desea su viejo profesor, Bohumil Med.   </p>
