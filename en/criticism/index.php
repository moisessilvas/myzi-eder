        <b><p style="text-align: center">Critique wrote by the Czech Bohumil Med on September 2nd, 2019, he is one of the greatest authorities of musical critique in Brazil. Published in his open group called "Coluna do Bohumil":</p></b>

        <p>The young conductor Mariana Menezes conducts the OSTNCS.</p>

        <p>A young conductor is usually received by the public with some reservations and misgivings. Even more when it comes to a woman. And when both factors are united - youth and female - apparently unfavorable, and yet presenting pieces from women compositors, the role of the conductor is very hard. But on the night of August 27th in the Brasilia Cinema, the conductor Mariana Meneses with only 30 years young transformed those negative expectations into a great success.</b></p>

        <p>Earning a bachelor of conduction from the University of Brasilia she continued studying in Brazil and abroad, collecting certifications and doing curses with famous conductors. Mariana's first public performance was in 2007 when she conducted a metal choir in the Emeritus Professor Title receiving solemnity from the University of Brasilia to this columnist. Other presentations occurred in National and International stages. Finally, Mariana had the opportunity to provide your art to your hometown Brasilia.</p>

        <p>At this concert, she privileged for women compositors, starting with <br>
          Fanny Mendelssohn Hensel, the famous composer Felix Mendelssohn's sister, with Overture in C Major. The slow initial part earned expressivity with flexible and soft gestures, typical from sensitive women. The most rhythmic part needed harder gestures but not solid. Already in this first piece, the conductor showed hand technique dominance, put to the service of the interpretation consistent with the character of the piece.</b>
        </p>

        <p>Then, Concertino for flute and orchestra, composed in 1902 by the French Cecile Chaminade aiming to give the flutists the opportunity to show virtuosity. The execution, with good tuning, was up to Mechthild Bier, first solo flute from the own orchestra, that interpreted brightly the difficult technical passages. The sound was beautiful, but not very expressive - even shy - a fact that depreciated a little the role of the soloist in relation to the orchestra, whose role was of simple accompaniment.</p>

        <p>The third piece privileged contemporary music. The author chosen was Cibele J. Donza, from the Brazilian state Pará, with the piece "Pasargada em Noites e Luzes", released in 2017, in Belém. Language from the XX century, a lot of percussion and sound effects. Inspired in the poem in which the topics were supposed to be - according to the program - heat, cold nights, dance, lights, and the fire, it's a pragmatic piece, interesting and well done inside the choosing style, but besides all the effort of the musicians and of the conductor, this columnist, maybe a little outdated, did not feel the emotions indicated by the program.</p>

        <p>The crowning was the symphony in Mi minor - Gaelic, by Amy Beach, the first symphony written by an American composer (1894). The sources to the piece were Irish folk songs developed in the four movements. With no doubt an interesting piece, pleasant, easy to listen, but not so easy to play much less so to conduct, but without the deepness of a Brahms or Beethoven. Here, the conductor presented qualities molding the sonority of the orchestra giving freedom to the soloists, in which the tubes were in the spotlight. She built the piece, gave her own touch, printed your conception.</p>

        <p>The conductor Mariana Menezes, physically young, but artistically adult, proved that talent, studies, dedication, and efforts result in an artistic maturity typical of the great masters. Prodigious memory, she rehearsed and conducted the whole program by heart. The sweetness dealing with the musicians and the deep knowledge, until the last detail, caused the admiration of the musicians and the public that gave a standing ovation. Pleased, and mainly, the presentation of a different program prepared in 4 days, only from women compositors proving that there is also equality between men and women in music and that it is earned with work and results, not with marches.</p>

        <p>We congratulate the young conductor that certainly will conquer great successes, not only in the provincial Brasília but in the national and international centers of musical culture. These are the wishes of your old professor, Bohumil Med.</p>
