        <b><p style="text-align: center">Critique wrote by the Czech Bohumil Med on September 2nd, 2019, he is one of the greatest authorities of musical critique in Brazil. Published in his open group called "Coluna do Bohumil":</p></b>

        <p>The young conductor Mariana Menezes conducts the OSTNCS.</p>

        <p>A young conductor usually has a reserved, suspicious reception by the public, particularly when the conductor is a woman. Moreover, when these two apparently downside factors – youth and female gender – are combined in a concert with works written exclusively by female composers, the conductor’s role is especially difficult. However, at concert night on August 27 at Cine Brasilia, the 30-year-old conductor Mariana Menezes had transformed such negative expectations into a great success.</b></p>

        <p>Earning a bachelor of conduction from the University of Brasilia she continued studying in Brazil and abroad, collecting certifications and doing curses with famous conductors. Mariana's first public performance was in 2007 when she conducted a metal choir in the Emeritus Professor Title receiving solemnity from the University of Brasilia to this columnist. Other presentations occurred in National and International stages. Finally, Mariana had the opportunity to provide your art to your hometown Brasilia.</p>

        <p>In this concert, she valued the work of four female composers, starting with Fanny Mendelssohn Hensel, sister of the famous composer Felix Mendelssohn, and her Overture on C Major. The initial slow segment was given deep expression with flexible, suave gesture, typical of sensitive women. Later, the rhythmic part required a firmer, though unhardened gesture. The conductor showed her ability on hand gesture technique yet in this first piece, which was rightly applied to an interpretation fitting to the work’s characteristics.
        </p>

        <p>Then, Concertino for flute and orchestra, composed in 1902 by the French Cecile Chaminade aiming to give the flutists the opportunity to show virtuosity. The execution, with good tuning, was up to Mechthild Bier, first solo flute from the own orchestra, that interpreted brightly the difficult technical passages. The sound was beautiful, but not very expressive - even shy - a fact that depreciated a little the role of the soloist in relation to the orchestra, whose role was of simple accompaniment.</p>

        <p>The third piece privileged contemporary music. The author chosen was Cibele J. Donza, from the Brazilian state Pará, with the piece "Pasargada em Noites e Luzes", released in 2017, in Belém. Language from the XX century, a lot of percussion and sound effects. Inspired in the poem in which the topics were supposed to be - according to the program - heat, cold nights, dance, lights, and the fire, it's a pragmatic piece, interesting and well done inside the choosing style, but besides all the effort of the musicians and of the conductor, this columnist, maybe a little outdated, did not feel the emotions indicated by the program.</p>

        <p>The concert’s apotheosis was the Symphony in E Minor “Gaelic” by Amy Beach, the very first symphony written by an American composer (1894). The inspiration for this work was songs from the Irish folklore, developed throughout the four movements. It is, without any doubt, an interesting, pleasurable piece, easy to listen to; but not so easy to perform nor conduct, even though it lacks the profundity of Brahms of Beethoven. Here, the conductor presented her qualities shaping the orchestra sound and allowing freedom for soloists, with prominence of the french horn set. She constructed the piece with her personality, imprinted on it her conception of it.</p>

        <p>The conductor Mariana Menezes, physically young, but artistically adult, proved that talent, studies, dedication, and efforts result in an artistic maturity typical of the great masters. Owner of a prodigious memory, she rehearsed and conducted the entire program by heart. The delicate way she treated the musicians and the profound knowledge she demonstrated to the least detail sparked admiration from all the orchestra and the public, who gave a standing ovation. Pleased, and mainly, the presentation of a different program prepared in 4 days, only from women compositors proving that there is also equality between men and women in music and that it is earned with work and results, not with marches.</p>

        <p>We congratulate the young conductor that certainly will conquer great successes, not only in the provincial Brasília but in the national and international centers of musical culture. These are the wishes of your old professor, Bohumil Med.</p>
